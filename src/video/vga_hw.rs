// Text buffer clear

// After mode 13h, planes 0 and 1 hold pixel data instead of text.
// Rather than saving/restoring (which requires trusting the BIOS VGA state
// before the first mode switch), we simply clear to a blank screen.
// The kernel will reprint its prompt on top.
//
// Called AFTER the main mode-03h register loop so GC[05]=0x10 (odd/even),
// GC[06]=0x0E (B8000 window), SR[02]=0x03 (planes 0+1), SR[04]=0x02
// are already active — matching the state used by put_char.

unsafe fn clear_text_buf() {
    let dst = 0xB8000usize as *mut u8;
    for i in 0..2000usize {
        dst.add(i * 2).write_volatile(0x20); // space
        dst.add(i * 2 + 1).write_volatile(0x07); // light-grey on black
    }
}

unsafe fn restore_font() {
    // Load the embedded 8×16 font into VGA plane 2.
    //
    // Must be called AFTER the main mode-03h register loop because the
    // sequencer reset (SR[00]=0x01) in that loop causes QEMU to reload its
    // internal default font, overwriting any earlier plane-2 write.
    //
    // Uses the exact BIOS INT 10h font-load register sequence:
    //   SR[00]=reset, SR[02]=plane-2, SR[04]=0x07, SR[00]=run,
    //   GC[04]=read-plane-2, GC[05]=write-mode-0, GC[06]=0x04 (A0000, no GFX bit)
    // Restores text-mode state afterwards so put_char() continues to work.
    port::write_u8(SEQ_INDEX, 0x00);
    port::write_u8(SEQ_DATA, 0x01); // sync reset
    port::write_u8(SEQ_INDEX, 0x02);
    port::write_u8(SEQ_DATA, 0x04); // plane 2 only
    port::write_u8(SEQ_INDEX, 0x04);
    port::write_u8(SEQ_DATA, 0x07); // ext-mem, no odd/even (BIOS value)
    port::write_u8(SEQ_INDEX, 0x00);
    port::write_u8(SEQ_DATA, 0x03); // release reset
    port::write_u8(GC_INDEX, 0x04);
    port::write_u8(GC_DATA, 0x02); // read plane 2
    port::write_u8(GC_INDEX, 0x05);
    port::write_u8(GC_DATA, 0x00); // write mode 0, no odd/even
    port::write_u8(GC_INDEX, 0x06);
    port::write_u8(GC_DATA, 0x04); // A0000-AFFFF, NOT graphics bit

    let vram = 0xA0000usize as *mut u8;

    for c in 0usize..256 {
        for row in 0usize..16 {
            vram.add(c * 32 + row)
                .write_volatile(VGA_FONT_8X16[c * 16 + row]);
        }
        for pad in 0usize..16 {
            vram.add(c * 32 + 16 + pad).write_volatile(0);
        }
    }

    // Restore sequencer and GC to normal text-mode state
    port::write_u8(SEQ_INDEX, 0x00);
    port::write_u8(SEQ_DATA, 0x01); // sync reset
    port::write_u8(SEQ_INDEX, 0x02);
    port::write_u8(SEQ_DATA, 0x03); // planes 0+1
    port::write_u8(SEQ_INDEX, 0x04);
    port::write_u8(SEQ_DATA, 0x02); // odd/even enabled
    port::write_u8(SEQ_INDEX, 0x00);
    port::write_u8(SEQ_DATA, 0x03); // release reset
    port::write_u8(GC_INDEX, 0x04);
    port::write_u8(GC_DATA, 0x00); // read plane 0
    port::write_u8(GC_INDEX, 0x05);
    port::write_u8(GC_DATA, 0x10); // odd/even mode
    port::write_u8(GC_INDEX, 0x06);
    port::write_u8(GC_DATA, 0x0E); // B8000 window
}

// DAC restoration for text mode

// AC_03 maps attribute nibbles to DAC indices: 0-5 sequentially, AR[06]=20,
// AR[07]=7, and AR[08]-AR[0F] = 56-63.  After a graphics session those DAC
// entries hold arbitrary colours; write the standard 18-bit VGA values back.

unsafe fn restore_text_dac() {
    const LO: [(u8, u8, u8); 8] = [
        (0, 0, 0),
        (0, 0, 42),
        (0, 42, 0),
        (0, 42, 42),
        (42, 0, 0),
        (42, 0, 42),
        (42, 21, 0),
        (42, 42, 42),
    ];
    port::write_u8(0x3C8, 0);
    for (r, g, b) in LO {
        port::write_u8(0x3C9, r);
        port::write_u8(0x3C9, g);
        port::write_u8(0x3C9, b);
    }
    // Index 20 = dark yellow (referenced by AR[06] = 0x14)
    port::write_u8(0x3C8, 20);
    port::write_u8(0x3C9, 42);
    port::write_u8(0x3C9, 42);
    port::write_u8(0x3C9, 0);
    // Indices 56-63 = bright colours (referenced by AR[08]-AR[0F] = 0x38-0x3F)
    const HI: [(u8, u8, u8); 8] = [
        (21, 21, 21),
        (21, 21, 63),
        (21, 63, 21),
        (21, 63, 63),
        (63, 21, 21),
        (63, 21, 63),
        (63, 63, 21),
        (63, 63, 63),
    ];
    port::write_u8(0x3C8, 56);
    for (r, g, b) in HI {
        port::write_u8(0x3C9, r);
        port::write_u8(0x3C9, g);
        port::write_u8(0x3C9, b);
    }
}

// Mode-switch routine

/// Program VGA hardware registers to switch to `mode`.
///
/// Supported modes:
///  - 0x03: 80×25 color text
///  - 0x0D: 320×200 16-color planar (EGA-style)
///  - 0x12: 640×480 16-color planar
///  - 0x13: 320×200 256-color (classic mode 13h)
///
/// Returns `true` on success, `false` for an unrecognised mode.
/// After a successful call, VRAM at physical 0xA0000 is the active display
/// for graphical modes; for 0x03, 0xB8000 (VGA text) is the active display.
pub unsafe fn set_video_mode(mode: u8) -> bool {
    let (misc, seq, crtc, gc, ac): (u8, &[(u8, u8)], &[(u8, u8)], &[(u8, u8)], &[(u8, u8)]) =
        match mode {
            0x03 => (MISC_03, SEQ_03, CRTC_03, GC_03, AC_03),
            0x0D => (MISC_0D, SEQ_0D, CRTC_0D, GC_0D, AC_0D),
            0x12 => (MISC_12, SEQ_12, CRTC_12, GC_12, AC_12),
            0x13 => (MISC_13, SEQ_13, CRTC_13, GC_13, AC_13),
            _ => return false,
        };

    // Write Miscellaneous Output while sequencer is in synchronous reset.
    port::write_u8(SEQ_INDEX, 0x00);
    port::write_u8(SEQ_DATA, 0x01); // synchronous reset
    port::write_u8(MISC_WRITE, misc);

    // Write Sequencer registers SR[01]–SR[04], then release reset.
    for &(idx, val) in seq {
        port::write_u8(SEQ_INDEX, idx);
        port::write_u8(SEQ_DATA, val);
    }
    port::write_u8(SEQ_INDEX, 0x00);
    port::write_u8(SEQ_DATA, 0x03); // normal operation

    // Unlock CRTC (clear the vertical-retrace protect bit in CR[0x11]).
    port::write_u8(CRTC_INDEX, 0x11);
    let cr11 = port::read_u8(CRTC_DATA);
    port::write_u8(CRTC_DATA, cr11 & 0x7F);

    // Write CRTC registers CR[0x00]–CR[0x18].
    for &(idx, val) in crtc {
        port::write_u8(CRTC_INDEX, idx);
        port::write_u8(CRTC_DATA, val);
    }

    // Write Graphics Controller registers GR[0x00]–GR[0x08].
    for &(idx, val) in gc {
        port::write_u8(GC_INDEX, idx);
        port::write_u8(GC_DATA, val);
    }

    // Write Attribute Controller registers AR[0x00]–AR[0x14].
    // Reading INPUT_STATUS1 resets the internal index/data flip-flop.
    let _ = port::read_u8(INPUT_STATUS1);
    for &(idx, val) in ac {
        port::write_u8(AC_WRITE, idx); // flip-flop → address mode
        port::write_u8(AC_WRITE, val); // flip-flop → data mode
    }
    // Enable video output (PAS = 1, index = 0 — display enabled).
    port::write_u8(AC_WRITE, 0x20);

    // After returning to text mode:
    //  1. clear planes 0+1 (mode 13h pixel data is now there)
    //  2. restore font to plane 2 — must be LAST because the main loop above
    //     includes SR[00]=sync-reset which causes QEMU to reload its default
    //     BIOS font; loading our font after that reset wins.
    //  3. restore DAC
    if mode == 0x03 {
        clear_text_buf();
        restore_font();
        restore_text_dac();
    }

    true
}
